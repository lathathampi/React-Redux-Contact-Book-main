#QN-E03: Write a program which accepts the value of radius of a circle,
#after which it should display the Area,Perimeter.
Radius =float(input("Enter the radius of a circle:"))
Area=round(3.14*Radius*Radius,2)
Perimeter=round(2*3.14*Radius,2)
print("Area of Circle:\t\t",Area,"\nPerimeter of Circle:\t",Perimeter)

